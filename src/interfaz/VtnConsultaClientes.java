    public void cargaComboSucursales()
    {
        comboSucursales.addItem("GLOBAL");
        listarCines = ctrl.leerTodosCines();

        for (Cines cn : listarCines)
        {
            comboSucursales.addItem(cn.getIdCine());
        }
    }

    public void listarClientesPorFiltro()
    {
        model = (DefaultTableModel) tblClientes.getModel();
        Object[] obj = new Object[7];
        model.setRowCount(0);
        listarClientes = ctrl.leerTodosClientes();

        for (Clientes cs : listarClientes)
        {            
                if (cs.getIdCine().equals(comboSucursales.getSelectedItem().toString()))
                {
                    obj[0] = cs.getIdCliente();
                    obj[1] = cs.getIdCine();
                    obj[2] = cs.getNombre();
                    obj[3] = cs.getPrimerApellido();
                    obj[4] = cs.getSegundoApellido();
                    obj[5] = cs.getCorreoElectronico();
                    obj[6] = cs.getTelefono();
                    model.addRow(obj);
                } else if ("GLOBAL".equals(comboSucursales.getSelectedItem().toString()))
                {
                    obj[0] = cs.getIdCliente();
                    obj[1] = cs.getIdCine();
                    obj[2] = cs.getNombre();
                    obj[3] = cs.getPrimerApellido();
                    obj[4] = cs.getSegundoApellido();
                    obj[5] = cs.getCorreoElectronico();
                    obj[6] = cs.getTelefono();
                    model.addRow(obj);
                }
        }
        tblClientes.setModel(model);
    }

