    private void btnSubirActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSubirActionPerformed
    {//GEN-HEADEREND:event_btnSubirActionPerformed

        String clave = txtcve_pelicula.getText();
        String nombre = txtnombre_pelicula.getText();
        String fecha = txt_fechaestreno.getText();
        String idioma = txtIdIdioma.getText();
        String subtitulos = txtSubtitulos.getText();
        String duracion = txtDuracion.getText();
        String sipnosis = txtSipnosis.getText();
        String reparto = txtReparto.getText();
        String trailer = txtTrailer.getText();
        String foto= txtFoto.getText();
        
        if (clave.isEmpty() || nombre.isEmpty() || fecha.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Por favor, complete los campos obligatorios (clave, nombre y fecha de estreno).");
            return;
        }
        Peliculas objpeli = new Peliculas(fecha, nombre, fecha, idioma, duracion, subtitulos, duracion, sipnosis, reparto, trailer, foto);
        PeliculaDatos.addMovie(objpeli);
        JOptionPane.showMessageDialog(this, "Película registrada exitosamente.");
        txtcve_pelicula.setText("");
        txtnombre_pelicula.setText("");
        txt_fechaestreno.setText("");
        txtIdIdioma.setText("");
        txtSubtitulos.setText("");
        txtDuracion.setText("");
        txtSipnosis.setText("");
        txtReparto.setText("");
        txtTrailer.setText("");
        txtFoto.setText("");
    }//GEN-LAST:event_btnSubirActionPerformed

