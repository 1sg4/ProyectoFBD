    private void btnSelectFotoActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSelectFotoActionPerformed
    {//GEN-HEADEREND:event_btnSelectFotoActionPerformed
        // Crear un JFileChooser para seleccionar la imagen
    JFileChooser chooser = new JFileChooser();
    // Filtrar para que solo se muestren archivos de imagen
    FileNameExtensionFilter filter = new FileNameExtensionFilter("Imágenes", "jpg", "jpeg", "png", "gif");
    chooser.setFileFilter(filter);
    
    int result = chooser.showOpenDialog(this);
    if (result == JFileChooser.APPROVE_OPTION) {
        File file = chooser.getSelectedFile();
        // Obtenemos la ruta absoluta del archivo
        String path = file.getAbsolutePath();
        // Mostrar la ruta en el JTextField
        txtFoto.setText(path);
        
        // Creamos un ImageIcon a partir de la ruta (no olvidar anteponer "file:" si fuera necesario, aquí usamos directamente la ruta)
        ImageIcon icon = new ImageIcon(path);
        
        // Redimensionar la imagen para que se ajuste al tamaño del JLabel (lblFoto)
        Image img = icon.getImage().getScaledInstance(lblFoto.getWidth(), lblFoto.getHeight(), Image.SCALE_SMOOTH);
        lblFoto.setIcon(new ImageIcon(img));
    }
    }//GEN-LAST:event_btnSelectFotoActionPerformed

